package admin;

import common.SimpleRover;
import java.io.IOException;
import java.io.PrintWriter;
import java.util.ArrayList;
import java.util.HashSet;
import java.util.List;
import java.util.Set;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import redcloud.db.DbConnect;
import redcloud.db.DbManager;

public class GetTeamUnassigned extends HttpServlet {
   
    /** 
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code> methods.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {

        final int teamNum = Integer.parseInt(request.getParameter("tnum"));

        final DbManager dbm = new DbManager("jdbc/moot");
        dbm.open();

        final DbConnect dbc = dbm.doQuery("SELECT rover_id, CONCAT(first_name,' ',last_name) AS full_name, rover_crew FROM rover WHERE rover_id NOT IN (SELECT rover_id FROM rover_team) ORDER BY rover_id");

        List<SimpleRover> roverList = new ArrayList<SimpleRover>();
        int maxLen = 10;

        while(dbc.next()) {
            int rover_id = dbc.getInt("rover_id");
            String full_name = dbc.getString("full_name");
            roverList.add(new SimpleRover(rover_id, full_name, dbc.getString("rover_crew")));
            maxLen = Math.max(maxLen, full_name.length());
        }

        dbc.endQuery();
        dbm.close();

        response.setContentType("text/html");
        PrintWriter out = response.getWriter();

        out.printf("<select id='team%d_add_select'>", teamNum);
        for(SimpleRover rover: roverList) {
            out.printf("<option value=%d>%<d - %s (%s)</option>", rover.rover_id, rover.full_name, rover.rover_crew);
        }
        out.print("</select>");
        out.printf("<button onclick='addMember(%d); return false;'> Add</button>", teamNum);
        out.flush();
    } 

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        processRequest(request, response);
    } 

    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        processRequest(request, response);
    }

    /** 
     * Returns a short description of the servlet.
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>
}
